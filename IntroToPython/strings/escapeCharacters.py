#!/usr/bin/env python3

# Escape characters by placing / before the character
txt = "Her nickname is \"Ciku\""
print(txt)

# Escape a single quote
txt = "I don\'t like you"
print(txt)

# Escape backslash
txt = "You are supposed to insert \\ as a backslash"
print(txt)

# Escape new line
txt = "Hello!\nI will call in the morning."
print(txt)
